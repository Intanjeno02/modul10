class Transaksi {
    String namaPemesan;
    String judulFilm;
    String noKursi;
    double harga;

    Transaksi(String namaPemesan, String judulFilm, String noKursi, double harga) {
        this.namaPemesan = namaPemesan;
        this.judulFilm = judulFilm;
        this.noKursi = noKursi;
        this.harga = harga;
    }
}